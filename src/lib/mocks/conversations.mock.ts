// ---------------------------------------------------------------------------
// PARSED MESSAGES (for WhatsApp view)
// ---------------------------------------------------------------------------
export function parseConversationMessages(conversation: Types.Conversation): Types.Message[] {
  if (!conversation.history) return [];
  const lines = conversation.history.split('\n').filter(l => l.trim());
  return lines.map((line, i) => {
    const isBot = line.startsWith('Maya:');
    const content = line.replace(/^(Maya|[A-Za-z]+):\s*/, '');
    return {
      id: `${conversation.id}-M${i}`,
      sender: isBot ? 'bot' : 'lead',
      content,
      timestamp: new Date(new Date(conversation.created_at).getTime() + i * 3600000 * 4).toISOString(),
    };
  });
}

